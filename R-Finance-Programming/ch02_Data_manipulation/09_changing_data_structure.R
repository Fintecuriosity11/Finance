### 데이터 구조 변환

# 아래의 함수를 이용하면 데이터 구조 변환 가능.

# as.data.frame  = 데이터 프레임으로 변환.
# as.list        = 리스트로 변환.
# as.matrix      = 행렬로 변환.
# as.vector      = 벡터로 변환.

fac <- factor(c("1","2","1","2"))
as.numeric(as.character(fac))

# 팩터를 정수로 변환하는 것

############################################################결과값(print)#################################################################

# > fac <- factor(c("1","2","1","2"))
# > as.numeric(as.character(fac))
# [1] 1 2 1 2

