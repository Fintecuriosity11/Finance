### 리스트(list)

### [[x]] 과 [x]의 차이.

# [[x]]은 리스트의 n번째 원소를 뜻합니다. 반면 [x]는 리스트입니다. 
# [x]의 결괏값은 리스트로 나오고, [[x]]의 결괏값은 숫자입니다.

# 즉 두 경우는 클래스(class)가 다릅니다.
# x의 원소를 리스트 형태로 추출하려면 "[]"를 사용하고, 벡터(또는 원소가 하나라면 스칼라) 형태로 추출하려면 "[[ ]]"를 사용.

x<-list(1,2,"3","good")

class(x[2])

class(x[[2]])

# typeof함수와 동일한 결과값이 나옵니다.

############################################################결과값(print)#################################################################

# > class(x[2])
# [1] "list"

# > 

# > class(x[[2]])
# [1] "numeric"

##########################################################################################################################################

x<-list(2,4,7)
x

x[[2]]<-NULL
x

# 리스트에서 원소를 삭제할 경우 해당원소에 NULL을 입력.

############################################################결과값(print)#################################################################

# > x<-list(2,4,7)

# > x
# [[1]]
# [1] 2

# [[2]]
# [1] 4

# [[3]]
# [1] 7

# > 

#  > x[[2]]<-NULL

# > x
# [[1]]
# [1] 2

# [[2]]
# [1] 7

##########################################################################################################################################

