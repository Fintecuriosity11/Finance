### las 인수.

# las는 축눈금 라벨(tick-marks labels)의 방향을 지정.
# 기본값은 0이고 축에 평행한 방향을 의미.
# 항상 수평방향은 1, 축에 수직방향은 2, 항상 수직방향은 3.



par("las")

x<-rnorm(200)
y<-25-22*x+5*x^2+rnorm(200)
par(mfrow=c(1,2))
plot(x,y)
par(las=2)
plot(x,y)



graphics.off()                    # 그래프 지워주는 함수.


