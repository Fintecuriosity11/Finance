### text.

# text(x,y,labels...) 함수는 지정된 문자를 (x,y) 좌표에 출력.


x<-1:5
y<-sin(x)
plot(x,y,col='red')
curve(sin(x), add = TRUE)
text(x[3]+0.7,y[3],"<-this is x[3],y[3]")
  
  

graphics.off()                    # 그래프 지워주는 함수.


