### 두개의 그래프 겹쳐서 그리기.

## 다른 방법은 함께 그리고자 하는 데이터 시리즈를 하나의 행렬로 묶어주고 matplot 함수를 이용해 그래프.

x<-seq(-3,3,0.02)
y1<-dnorm(x)
y2<-dt(x,df=1)
ymat<-cbind(y1,y2)
matplot(x,ymat,type='l', bty='n')   # student t 분포 함수.

graphics.off()                      # 그래프를 지워주는 함수.


