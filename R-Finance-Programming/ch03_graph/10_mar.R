### par함수

# par 함수를 이용하면 그리기 관련 모수(plotting parameter)를 설정을 할 수 있음.
# par 함수를 실행한 뒤의 그래프부터 적용.

# par 함수의 인수 중 축눈금, 축라벨 등과 관련된 인수 확인.


##########################################################################################################################################

### mar 인수.

# mar은 여백(margin)의 크기를 지정하는 인수로 4개의 값을 가짐.
# 순서대로 아래, 왼쪽, 위 그리고 오른쪽 순서.
# x 축부터 시작해서 시계방향으로 회전하는 순서로 4개의 값을 지정.
# 기본값은 (5.1, 4.1, 4.1, 2.1)

par("mar")


x<-rnorm(200)
y<-25-22*x+5*x^2+rnorm(200)
par(mfrow=c(1,2))
plot(x,y)
par(mar=c(4,3,2,1))
plot(x,y)


graphics.off()                    # 그래프 지워주는 함수.


