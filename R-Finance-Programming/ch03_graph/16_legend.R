### legend

# legend 함수를 이용하면 범례를 설정할 수 있음.
# legend 함수의 첫 번째, 두 번째 인수는 x좌표와 y좌표.

# 좌표 대신 다음 문자열을 x좌표에 투입하고, y좌표를 생략하면 미리 정해진 위치에 범례를 표시할 수 있음.

# pch = 점 모양을 지정.
# lty = 선 모양을 지정.
# lwd = 선 굵기를 지정.
# col = 색깔을 지정.

x<-seq(-1,0,0.05)
plot(x,x,type="l", col=1, lty=1)
for(i in 2:4) lines(x,x^i, col=i, lty=i)
legend('bottomright', c("1","2","3","4"),col=1:4,lty = 1:4)


graphics.off()                    # 그래프 지워주는 함수.


