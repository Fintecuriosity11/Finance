### axis 함수

# axis 함수를 이용하면 축에 대한 설정을 할 수 있음.
# 첫 인수는 어느 축인지를 지정하는 것으로 아래쪽 축부터 시계방향으로 1,2,3,4 값을 가짐.
# 다른 인수는 축의 위치, 축눈금의 위치, 라벨 등을 지정.
# plot 함수의 인수 중에 axes = FALSE 로 지정하거나 XY 축 중 특정 축에만 적용할 경우 xaxt='n' 또는 yaxt = 'n'을 지정한 뒤 사용가능.


mat<-c(0,5,10,15,20)
plot(mat, xaxt='n')
lab<-c("a","b","c","d","end")
axis(side=1, at=seq(1,5,1), labels = lab)     # 1 번 축(x축)의 원래 (1,2,3,4,5) 자리 대신에 lab벡터("a","b","c","d","end")를 넣는다는 의미

graphics.off()                    # 그래프 지워주는 함수.


