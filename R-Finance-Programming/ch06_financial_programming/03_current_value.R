### 현재가치

# 수익률이 주어지면 미래가치와 반대로 미래자금에 대한 현재시점의 가치(현재가치)를 알 수 있음.
# 이전 미래가치 예제와 똑같이 연복리 10 %인 경우에는 다양한 미래시점 1(원)에 대한 현재가치를 산출.

T<-1:50          # T는 시간 (50년 뒤)
r<-0.1           # r = interest rate, 이자율
PV<-1/(1+r)^T      # 현재가치 = 1/(1+이자율)^시간   , PV=1/(1+0.1)^50
plot(T, PV, type='l')


############################################################결과값(print)#################################################################

