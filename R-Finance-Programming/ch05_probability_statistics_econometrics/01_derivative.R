### 미분(Derivative)

# 미분은 추가 패키지 없이 기본적으로 수행 가능
# 2차 방정식에 대한 미분 실행 , 
# f(x) = x^2

# 미분은 기본적으로 D함수를 이용. 1차 미분은 아래와 같음.

fx=expression(x^2,"x")
D(fx,'x')



############################################################결과값(print)#################################################################

# > fx=expression(x^2,"x")
# > D(fx,'x')
# 2 * x


##########################################################################################################################################


# 2차 미분은 D 함수를 2번 적용하면 됨.

fx=expression(x^2,"x")
D(D(fx,'x'),'x')



############################################################결과값(print)#################################################################

# > fx=expression(x^2,"x")
# > D(D(fx,'x'),'x')
# [1] 2


##########################################################################################################################################

# 복잡한 수식의 미분을 생각.

# f(X) = x^5 -1/x + cos(X)^x

fx = expression(x^5/x+cos(x)^x,'x')s
D(fx,'x')

############################################################결과값(print)#################################################################

# > fx = expression(x^5/x+cos(x)^x,'x')
# > D(fx,'x')
# 5 * x^4/x - x^5/x^2 + (cos(x)^x * log(cos(x)) - cos(x)^(x - 1) * (x * sin(x)))


