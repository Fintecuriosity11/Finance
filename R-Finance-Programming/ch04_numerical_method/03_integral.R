# 구체적으로 다음 함수를 구간(0, ∞) 에서 적분.

# y = 1/(x+1)√x

# 위의 함수는 다음과 같이 생김.

integrand<-function(x) 1/((x+1)*sqrt(x))
curve(integrand)


########################################################결과값(print)#####################################################################

# 그래프 참조.

##########################################################################################################################################

# integrate 함수를 이용하여 적분.

integrand<- function(x) 1/((x+1)*sqrt(x))
integrate(integrand, lower = 0, upper=Inf)

########################################################결과값(print)#####################################################################

# > integrand<- function(x) 1/((x+1)*sqrt(x))
# > integrate(integrand, lower = 0, upper=Inf)
# 3.141593 with absolute error < 2.7e-05

